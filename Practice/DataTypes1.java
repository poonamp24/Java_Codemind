public class DataTypes1
{

	public static void main(String []args){
		int a= 90;
		long b = 100;
		float c= 22.5f;
		double d = 8.987763d;
		char e = 'P';
		byte f = 3;
		short g = 9;
		boolean h = false;
	
		System.out.println("Integer Value          :"+ a);
		System.out.println("Long Integer value is  :"+ b);
		System.out.println("Floating value is      :"+ c);
		System.out.println("Double value is        :"+ d);
		System.out.println("Character value is     :"+ e);
		System.out.println("Byte value is          :"+ f);
		System.out.println("Short value is         :"+ g );
		System.out.println("Boolean's value is     :"+ h );
		
		
	}
	}